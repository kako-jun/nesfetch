// VBlank待機（NMI有効時はフラグベース、無効時はポーリング）
// 注意: PPU_CTRL ($2000) は書き込み専用。読み取りはopen bus。影レジスタで判定
void wait_vblank(void) {
    if (ppu_ctrl_shadow & 0x80) {
        while (!nmi_ready);
        nmi_ready = 0;
    } else {
        while ((PPU_STATUS & 0x80) == 0);
    }
}

// PPUアドレス設定
void set_ppu_addr(unsigned int addr) {
    PPU_ADDR = (unsigned char)(addr >> 8);
    PPU_ADDR = (unsigned char)(addr & 0xFF);
}

// パレット読み込み
void load_palette(void) {
    unsigned char i;
    set_ppu_addr(0x3F00);
    for (i = 0; i < 32; i++) {
        PPU_DATA = palette[i];
    }
}

// 画面クリア（ネームテーブル960バイト＋属性テーブル64バイト）
void clear_screen(void) {
    unsigned int i;
    set_ppu_addr(0x2000);
    for (i = 0; i < 1024; i++) {
        PPU_DATA = 0x00;
    }
}

// テキスト描画
void draw_text(unsigned char x, unsigned char y, const unsigned char* text) {
    unsigned int addr = 0x2000 + (y << 5) + x;
    unsigned char i = 0;

    set_ppu_addr(addr);
    while (text[i] != 0x00 && i < 32) {
        PPU_DATA = text[i];
        i++;
    }
}

// タイトル
const unsigned char title_text[] = {0x2E, 0x25, 0x33, 0x26, 0x25, 0x34, 0x23, 0x28, 0x00};  // NESFETCH

// ロゴ表示（メイン画面 — 上下キーで直接切替）
void draw_logo_screen(void) {
    unsigned char x, y, len;
    unsigned char base_tile;

    wait_vblank();
    PPU_MASK = 0x00;

    clear_screen();

    // タイトル
    draw_text(11, 2, title_text);

    // コンソール名（中央寄せ）
    len = 0;
    while (logo_names[current_logo][len] != 0x00 && len < 16) len++;
    draw_text((32 - len) / 2, 5, logo_names[current_logo]);

    // ロゴタイルパターン描画（3×4タイル = 24×32ピクセル）
    base_tile = 0x40 + (current_logo * 12);

    for (y = 0; y < 4; y++) {
        set_ppu_addr(0x2000 + ((10 + y) << 5) + 14);
        for (x = 0; x < 3; x++) {
            PPU_DATA = base_tile + (y * 3) + x;
        }
    }

    // ナビゲーション矢印（常に表示、ラップアラウンドするため）
    set_ppu_addr(0x2000 + (7 << 5) + 15);
    PPU_DATA = 0x3E;  /* ▲ */
    set_ppu_addr(0x2000 + (16 << 5) + 15);
    PPU_DATA = 0x3D;  /* ▼ */

    PPU_SCROLL = 0;
    PPU_SCROLL = 0;
    PPU_MASK = 0x1E;
}

// コントローラー読み取り
unsigned char read_controller(void) {
    unsigned char i;
    unsigned char buttons = 0;

    CONTROLLER1 = 1;
    CONTROLLER1 = 0;

    for (i = 0; i < 8; i++) {
        buttons = (buttons >> 1) | ((CONTROLLER1 & 0x01) << 7);
    }

    return buttons;
}

/* ゲームメイン */
void game_main(void) {
    unsigned char pressed;

    /* PPU初期化 */
    ppu_ctrl_shadow = 0x00;
    PPU_CTRL = ppu_ctrl_shadow;
    PPU_MASK = 0x00;

    wait_vblank();

    load_palette();
    draw_logo_screen();

    ppu_ctrl_shadow = 0x80;  /* NMI有効 */
    PPU_CTRL = ppu_ctrl_shadow;
    PPU_MASK = 0x1E;  /* 描画有効 */

    pad1_prev = 0;

    /* メインループ — 上下キーで直接ロゴ切替 */
    while (1) {
        wait_vblank();

        pad1 = read_controller();
        pressed = pad1 & ~pad1_prev;

        if (pressed & BTN_UP) {
            if (current_logo > 0) {
                current_logo--;
            } else {
                current_logo = total_logos - 1;  /* ラップアラウンド */
            }
            draw_logo_screen();
        }

        if (pressed & BTN_DOWN) {
            if (current_logo < total_logos - 1) {
                current_logo++;
            } else {
                current_logo = 0;  /* ラップアラウンド */
            }
            draw_logo_screen();
        }

        pad1_prev = pad1;
    }
}
